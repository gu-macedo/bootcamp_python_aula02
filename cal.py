def soma():
    x = int(input("Primeiro número da soma: "))
    y = int(input("Segundo número da soma: "))
    return print(x + y)
    
def resto_5():
    x = int(input("Resto 5: "))
    return print(int(x/5))

def multiplicacao(): 
    x = int(input("Primeiro número da multiplicação: "))
    y = int(input("Segundo número da multiplicação: ")) 
    return print(x*y)

def divisao(): 
    x = int(input("Primeiro número da divisão: "))
    y = int(input("Segundo número da divisão: ")) 
    return print(int(x/y))             

def quadrado():
    x = int(input("Número para ser elevado ao quadrado: ")) 
    return print(x**2)    